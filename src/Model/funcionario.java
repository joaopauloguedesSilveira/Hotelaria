package Model;

/**
 *
 * @author joaop
 */
public class funcionario {
    private int id;
    private String cargo;
    private String cpf;
    private String nome;
    private float salario;

    public funcionario() {
    }

    public funcionario(int id, String cargo, String cpf, String nome, float salario) {
        this.id = id;
        this.cargo = cargo;
        this.cpf = cpf;
        this.nome = nome;
        this.salario = salario;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCargo() {
        return cargo;
    }

    public void setCargo(String cargo) {
        this.cargo = cargo;
    }

    public String getCpf() {
        return cpf;
    }

    public void setCpf(String cpf) {
        this.cpf = cpf;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public float getSalario() {
        return salario;
    }

    public void setSalario(float salario) {
        this.salario = salario;
    }
    
    
}
